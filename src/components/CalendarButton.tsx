export default function CalendarButton({ timeframe, onChange }: {
    timeframe: [Date, Date];
    onChange: ([start, end]: [Date, Date]) => void;
}) {
    return (
        <DateRangePicker initialTimeframe={timeframe} onChange={onChange} />
    )
}

export function DateRangePicker({ initialTimeframe, onChange, className }: {
    initialTimeframe: [Date, Date];
    onChange: ([start, end]: [Date, Date]) => void;
    className?: string;
}) {
    const [date, setDate] = useState<DateRange | undefined>({
        from: initialTimeframe[0],
        to: initialTimeframe[1],
    });

    function handleSelect(date: DateRange | undefined) {
        if (date?.from && date.to) {
            onChange([date.from, date.to]);
        }
        setDate(date);
    }

    return (
        <div className={cn("grid gap-2", className)}>
            <Popover>
                <PopoverTrigger asChild>
                    <Button
                        id="date"
                        variant={"outline"}
                        className={cn(
                            "w-[300px] justify-start text-left font-normal",
                            !date && "text-muted-foreground"
                        )}
                    >
                        <FontAwesomeIcon icon={faCalendar} className="mr-2 h-4 w-4" />
                        {date?.from ? (
                            date.to ? (
                                <>
                                    {format(date.from, "LLL dd, y")} -{" "}
                                    {format(date.to, "LLL dd, y")}
                                </>
                            ) : (
                                format(date.from, "LLL dd, y")
                            )
                        ) : (
                            <span>Pick a date</span>
                        )}
                    </Button>
                </PopoverTrigger>
                <PopoverContent className="w-auto p-0 bg-white" align="start">
                    <Calendar
                        initialFocus
                        mode="range"
                        defaultMonth={date?.from}
                        selected={date}
                        onSelect={handleSelect}
                        numberOfMonths={2}
                    />
                </PopoverContent>
            </Popover>
        </div>
    )
}

